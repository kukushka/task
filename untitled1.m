function pushbutton1_Callback(hObject, eventdata, handles)
global lines;
global N;
global numb;
%кнопка дл€ построени€ линии
if (N==0) 
    lines=cell(1,1); 
end;
ed5=findobj(gcf,'tag','edit5');
j=str2double(get(ed5,'string'));%ввели номер линии
numb(j)=j;
if (N<j) %≈сли номер линии больше максимального, максимальный увеличиваем на один и рисуем его, ибо нехрен рисовать сотую линию,когда у нас их всего 5, например, будем рисовать шестую
    N=N+1;
    j = N;
    set(handles.edit5,'String',num2str(j));
    lines{j} = myLine;
    lines{j}.x0 = str2double(get(handles.edit1,'String'));
    lines{j}.xn = str2double(get(handles.edit3,'String'));
    lines{j}.y0 = str2double(get(handles.edit2,'String'));
    lines{j}.yn = str2double(get(handles.edit4,'String'));
    [x, y]=lines{j}.draw();
    plot(x, y, 'parent',handles.axes1);
    text((lines{j}.x0+lines{j}.xn)*0.5,(lines{j}.y0+lines{j}.yn)*0.5,int2str(j));
    hold on;
else %≈сли лини€ с таким номером уже существует - закрашиваем еЄ белым и рисуем с новыми координатами
   [x, y]=lines{j}.draw();
   plot(x, y, 'parent',handles.axes1, 'color', 'white');
   text((lines{j}.x0+lines{j}.xn)*0.5,(lines{j}.y0+lines{j}.yn)*0.5,int2str(j), 'color', 'white');
   hold on; 
   lines{j}.x0 = str2double(get(handles.edit1,'String'));
   lines{j}.xn = str2double(get(handles.edit3,'String'));
   lines{j}.y0 = str2double(get(handles.edit2,'String'));
   lines{j}.yn = str2double(get(handles.edit4,'String'));
   [x, y]=lines{j}.draw();
   plot(x, y, 'parent',handles.axes1);
   text((lines{j}.x0+lines{j}.xn)*0.5,(lines{j}.y0+lines{j}.yn)*0.5,int2str(j));
   hold on;
end
